C*********************************************************************
C*     　衝突過程（単一時間緩和 BGK モデル) 　　　　　　　　         *
C*********************************************************************
       SUBROUTINE COLLI2
C---------------------------------------------------------------------
       INCLUDE 'lbshock.inc'
C
       DO 115 I=1,M-2
         DO 110 J=1,N-2
C
           CALL CALE(I,J,ROU,UO,VO,E)
           CALL CALCF(C,CF,E)
C---------------------------------------------------------------------
           DM0=F0(I,J)
           FE0=EQUIL(UO,VO,UO*UO+VO*VO,PI,0.0E0,CF(1,0),ROU,E,0,1)
           F0(I,J)=F0(I,J)-(F0(I,J)-FE0)/FAI
           DFALL=ABS(F0(I,J)-FE0)/ROU
C
           DO 105 K=1,4
             DM(1,1,K)=F11(I,J,K)
             DM(1,2,K)=F12(I,J,K)
             DM(1,3,K)=F13(I,J,K)
             DM(2,1,K)=F21(I,J,K)
             DM(2,2,K)=F22(I,J,K)
             FE(1,1,K)=EQUIL(UO,VO,UO*UO+VO*VO,PI,      C
     &                                         ,CF(1,1),ROU,E,K,1)
             FE(1,2,K)=EQUIL(UO,VO,UO*UO+VO*VO,PI,2.0E0*C
     &                                         ,CF(1,2),ROU,E,K,1)
             FE(1,3,K)=EQUIL(UO,VO,UO*UO+VO*VO,PI,3.0E0*C
     &                                         ,CF(1,3),ROU,E,K,1)
             FE(2,1,K)=EQUIL(UO,VO,UO*UO+VO*VO,PI,SQRT(2.0E0)*C
     &                                         ,CF(2,1),ROU,E,K,2)
             FE(2,2,K)=EQUIL(UO,VO,UO*UO+VO*VO,PI,2.0E0*SQRT(2.0E0)*C
     &                                         ,CF(2,2),ROU,E,K,2)
             F11(I,J,K)=F11(I,J,K)-(F11(I,J,K)-FE(1,1,K))/FAI
             F12(I,J,K)=F12(I,J,K)-(F12(I,J,K)-FE(1,2,K))/FAI
             F13(I,J,K)=F13(I,J,K)-(F13(I,J,K)-FE(1,3,K))/FAI
             F21(I,J,K)=F21(I,J,K)-(F21(I,J,K)-FE(2,1,K))/FAI
             F22(I,J,K)=F22(I,J,K)-(F22(I,J,K)-FE(2,2,K))/FAI
             DFALL=DFALL+( ABS(F11(I,J,K)-FE(1,1,K))
     &                    +ABS(F12(I,J,K)-FE(1,2,K))
     &                    +ABS(F13(I,J,K)-FE(1,3,K))
     &                    +ABS(F21(I,J,K)-FE(2,1,K))
     &                    +ABS(F22(I,J,K)-FE(2,2,K)) )/ROU
  105      CONTINUE
           IF(DFALL.GE.VNEQ) THEN
             F0(I,J)=DM0-(DM0-FE0)/(FAI+DFAI)
             DO 108 K=1,4
               F11(I,J,K)=DM(1,1,K)-(DM(1,1,K)-FE(1,1,K))/(FAI+DFAI)
               F12(I,J,K)=DM(1,2,K)-(DM(1,2,K)-FE(1,2,K))/(FAI+DFAI)
               F13(I,J,K)=DM(1,3,K)-(DM(1,3,K)-FE(1,3,K))/(FAI+DFAI)
               F21(I,J,K)=DM(2,1,K)-(DM(2,1,K)-FE(2,1,K))/(FAI+DFAI)
               F22(I,J,K)=DM(2,2,K)-(DM(2,2,K)-FE(2,2,K))/(FAI+DFAI)
  108        CONTINUE
           ENDIF
C---------------------------------------------------------------------
C
  110    CONTINUE
  115  CONTINUE
C
       RETURN
       END
