C*********************************************************************
C*     　副格子１での滑り壁の境界条件　　　　　　　　　　　　        *
C*********************************************************************
       SUBROUTINE S_BC1(F,M,N)
C---------------------------------------------------------------------
       REAL F(0:8000,0:10,4)
C
C------------------------------------------[ 上面 & 下面の境界 ]
       DO 460 I=1,M-2
         F(I,N-1,4)=F(I,N-1,2)
         F(I,0,2)=F(I,0,4)
  460  CONTINUE
C--------------------------------------------[ 左 & 右の境界 ]
       DO 470 J=1,N-2
         F(0,J,1)=F(0,J,3)
         F(M-1,J,3)=F(M-1,J,1)
  470  CONTINUE
C---------------------------------------------------------------------
C
       RETURN
       END
