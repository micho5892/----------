C*********************************************************************
C*     　局所平衡分布関数　　　　　　　　　　　                      *
C*********************************************************************
C---------------------------------------------------------------------
       REAL FUNCTION EQUIL(U,V,U2,PI,C,CF,ROU,E,K,L)
C---------------------------------------------------------------------
       REAL U,V,U2,PI,C,CF,E,ROU,THETA,CONST
C
       IF(L.EQ.1) THEN
         THETA=PI*(K-1)/2.0E0
       ELSE
         THETA=PI*((K-1)/2.0E0+0.25E0)
       ENDIF
       CONST=C*(COS(THETA)*U+SIN(THETA)*V)/E
       EQUIL=CF*ROU*( 1.0E0
     &               +      CONST
     &               +0.5E0*CONST*CONST
     &               -0.5E0*U2/E
     &               -0.5E0*CONST*U2/E
     &               +      CONST*CONST*CONST/6.0E0 )
C
       RETURN
       END
