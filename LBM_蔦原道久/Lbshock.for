       SUBROUTINE S_BC2(F,M,N)
C---------------------------------------------------------------------
       REAL F(0:8000,0:10,4)
C
C------------------------------------------[ 上面 & 下面の境界 ]
       DO 510 I=1,M-2
         F(I,N-1,3)=F(I,N-1,2)
         F(I,N-1,4)=F(I,N-1,1)
         F(I,0,1)=F(I,0,4)
         F(I,0,2)=F(I,0,3)
  510  CONTINUE
C------------------------------------------[ 左 & 右の境界 ]
       DO 520 J=1,N-2
         F(0,J,1)=F(0,J,2)
         F(0,J,4)=F(0,J,3)
         F(M-1,J,2)=F(M-1,J,1)
         F(M-1,J,3)=F(M-1,J,4)
  520  CONTINUE
C----------------------------------------------[ 4つのかどでの境界 ]
       F(0,N-1,4)=F(0,N-1,2)
       F(M-1,N-1,3)=F(M-1,N-1,1)
       F(0,0,1)=F(0,0,3)
       F(M-1,0,2)=F(M-1,0,4)
