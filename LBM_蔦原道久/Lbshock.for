C*********************************************************************
C* 　　局所平衡分布関数の係数の計算 　　　　　                       *
C*********************************************************************
       SUBROUTINE CALCF(C,CF,E)
C---------------------------------------------------------------------
       REAL C,CF(2,0:3),E
C
       CF(1,0)=1.0E0-5.0E0*E*( 17.0E0*E*E/(8.0E0*C*C*C*C)
     &                        -35.0E0*E  /(8.0E0*C*C)
     &                        +49.0E0    / 15.0E0      )/(6.0E0*C*C)
       CF(1,1)=0.25E0*E*( 13.0E0*E*E/(4.0E0*C*C*C*C)
     &                   -71.0E0*E  /(12.0E0*C*C)
     &                   +3.0E0  )/(C*C)
       CF(1,2)=0.125E0*E*(-1.25E0*E*E/(C*C*C*C)
     &                    +25.0E0*E/(12.0E0*C*C)
     &                    -0.6E0  )/(C*C)
       CF(1,3)=E*( 0.25E0*E*E/(C*C*C*C)
     &            -0.25E0*E  /(C*C)
     &            +1.0E0/15.0E0  )/(12.0E0*C*C)
       CF(2,1)=E*E*(-0.25E0*E/(C*C)+1.0E0/3.0E0)/(C*C*C*C)
       CF(2,2)=E*E*(E/(C*C)-1.0E0/3.0E0)/(64.0E0*C*C*C*C)
C
       RETURN
       END
