C*********************************************************************
C*    　 副格子１での粒子の並進　　　　　　　　　                    *
C*********************************************************************
       SUBROUTINE MOVE1(F,DMF,M,N)
C---------------------------------------------------------------------
       REAL F(0:8000,0:10,4),DMF(0:8000,0:10,4)
C
       DO 140 I=0,M-1
         DO 130 J=0,N-1
           DO 120 K=1,4
             DMF(I,J,K)=F(I,J,K)
  120      CONTINUE
  130    CONTINUE
  140  CONTINUE
C
C------------------------------------------------[ 1-DIRECTION ]
C    
       DO 210 J=0,N-1
         DO 200 I=M-1,1,-1
           F(I,J,1)=F(I-1,J,1)
  200    CONTINUE
  210  CONTINUE
C------------------------------------------------[ 2-DIRECTION ]
       DO 230 J=N-1,1,-1
         DO 220 I=1,M-2
           F(I,J,2)=F(I,J-1,2)
  220    CONTINUE
  230  CONTINUE
C------------------------------------------------[ 3-DIRECTION ]
C   
       DO 260 J=N-1,0,-1
         DO 250 I=0,M-2
           F(I,J,3)=F(I+1,J,3)
  250    CONTINUE
  260  CONTINUE
C------------------------------------------------[ 4-DIRECTION ]
       DO 290 J=0,N-2
         DO 280 I=1,M-2
           F(I,J,4)=F(I,J+1,4)
  280    CONTINUE
  290  CONTINUE
C---------------------------------------------------------------
C
       RETURN
       END
C
C
C*********************************************************************
C*     　副格子２での粒子の並進　　　　　　　　　                    *
C*********************************************************************
       SUBROUTINE MOVE2(F,DMF,M,N)
C---------------------------------------------------------------------
       REAL F(0:8000,0:10,4),DMF(0:8000,0:10,4)
C
       DO 310 I=0,M-1
         DO 305 J=0,N-1
           DO 300 K=1,4
             DMF(I,J,K)=F(I,J,K)
  300      CONTINUE
  305    CONTINUE
  310  CONTINUE
C
C------------------------------------------------[ 1-DIRECTION ]
       DO 320 J=N-1,1,-1
         DO 315 I=1,M-1
           F(I,J,1)=F(I-1,J-1,1)
  315    CONTINUE
  320  CONTINUE
C------------------------------------------------[ 2-DIRECTION ]
       DO 330 J=N-1,1,-1
         DO 325 I=0,M-2
           F(I,J,2)=F(I+1,J-1,2)
  325    CONTINUE
  330  CONTINUE
C------------------------------------------------[ 3-DIRECTION ]
       DO 350 J=0,N-2
         DO 340 I=0,M-2
           F(I,J,3)=F(I+1,J+1,3)
  340    CONTINUE
  350  CONTINUE
C------------------------------------------------[ 4-DIRECTION ]
       DO 370 J=0,N-2
         DO 360 I=1,M-1
           F(I,J,4)=F(I-1,J+1,4)
  360    CONTINUE
  370  CONTINUE
C---------------------------------------------------------------
C
       RETURN
       END
