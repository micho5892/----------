C*********************************************************************
C*     初期値の設定　　　　　　　　　　　　　　　　　　　　　　      *
C*********************************************************************
       SUBROUTINE INIT
C---------------------------------------------------------------------
       INCLUDE 'lbshock.inc'
C
C------[ 領域１での内部エネルギー（温度） ]
       T1=E0
C------[ 領域１での音速 ]
       S1=SQRT(GANMA*T1)
C------[ 領域１の流速 ]
       U1=XMS*S1
C------[ 領域１での密度 ]
       ROU1=ROU0
C------[ 領域１での圧力 ]
       P1=ROU1*T1
C------[ 領域２での内部エネルギー（温度） ]
       T2=T1*(2.0E0*GANMA*XMS*XMS-GANMA+1.0E0)
     &   *((GANMA-1.0E0)*XMS*XMS+2.0E0)
     &   /((GANMA+1.0E0)*(GANMA+1.0E0)*XMS*XMS)
C------[ 領域２での圧力 ]
       P2=P1*(2.0E0*GANMA*XMS*XMS-GANMA+1.0E0)/(GANMA+1.0E0)
C------[ 領域２での密度 ]
       ROU2=P2/T2
C------[ 領域２の流速 ]
       U2=ROU1*U1/ROU2
C------[ 領域２での音速 ]
       S2=S1*SQRT(T2/T1)
C
       FAI=XMYU/E0/TAU/ROU1+0.5E0
C       
       RE=ROU0*(U1-U2)*REAL(M-2)/XMYU
C
       S4=SQRT(GANMA*(E0+DE))
       ALPHA=(2.0E0*GANMA*XMS*XMS-GANMA+1.0E0)/(GANMA+1.0E0)
     &      *(1.0E0-(GANMA-1.0E0)*S1/(GANMA+1.0E0)/S4*(XMS-1.0E0/XMS)
     &       )**(-2.0E0*GANMA/(GANMA-1.0E0))
       ROU4=E0*ROU0/(E0+DE)*ALPHA
C   
C
       CALL CALCF(C,CF1,E0+DE)
       CALL CALCF(C,CF2,E0)
C
       DO 80 I=0,M-1
         DO 75 J=0,N-1
C
         IF(I.LE.M1-1) THEN
           F0(I,J)=EQUIL(0.0E0,0.0E0,0.0E0,PI,0.0E0
     &                              ,CF1(1,0),ROU4,E0+DE,0,1)
           DO 60 K=1,4
             F11(I,J,K)=EQUIL(0.0E0,0.0E0,0.0E0,PI,C
     &                             ,CF1(1,1),ROU4,E0+DE,K,1)
             F12(I,J,K)=EQUIL(0.0E0,0.0E0,0.0E0,PI,2.0E0*C
     &                             ,CF1(1,2),ROU4,E0+DE,K,1)
             F13(I,J,K)=EQUIL(0.0E0,0.0E0,0.0E0,PI,3.0E0*C
     &                             ,CF1(1,3),ROU4,E0+DE,K,1)
             F21(I,J,K)=EQUIL(0.0E0,0.0E0,0.0E0,PI,SQRT(2.0E0)*C
     &                             ,CF1(2,1),ROU4,E0+DE,K,2)
             F22(I,J,K)=EQUIL(0.0E0,0.0E0,0.0E0,PI,SQRT(8.0E0)*C
     &                             ,CF1(2,2),ROU4,E0+DE,K,2)
   60      CONTINUE
         ELSE
           F0(I,J)=EQUIL(0.0E0,0.0E0,0.0E0,PI,0.0E0
     &                                    ,CF2(1,0),ROU0,E0,0,1)
           DO 70 K=1,4
             F11(I,J,K)=EQUIL(0.0E0,0.0E0,0.0E0,PI,C
     &                                         ,CF2(1,1),ROU0,E0,K,1)
             F12(I,J,K)=EQUIL(0.0E0,0.0E0,0.0E0,PI,2.0E0*C
     &                                         ,CF2(1,2),ROU0,E0,K,1)
             F13(I,J,K)=EQUIL(0.0E0,0.0E0,0.0E0,PI,3.0E0*C
     &                                         ,CF2(1,3),ROU0,E0,K,1)
             F21(I,J,K)=EQUIL(0.0E0,0.0E0,0.0E0,PI,SQRT(2.0E0)*C
     &                                         ,CF2(2,1),ROU0,E0,K,2)
             F22(I,J,K)=EQUIL(0.0E0,0.0E0,0.0E0,PI,SQRT(8.0E0)*C
     &                                         ,CF2(2,2),ROU0,E0,K,2)
   70      CONTINUE
         ENDIF
C
   75    CONTINUE
   80  CONTINUE
C
       RETURN
       END
