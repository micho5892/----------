C*     副格子１における粒子の種類                                                              *
C*                                                                   *
C*                                                                   *
C*                                        F13(I,J,2)                 *
C*                                             |                     *
C*                                             |                     *
C*                                   F12(I,J,2)|                     *
C*        F11(I,J,2)                           |                     *
C*             |                               |                     *
C* F11(I,J,3)  |  F11(I,J,1)       F12(I,J,3)  |  F12(I,J,1)         *
C*        -----+-----         <-----<----------+---------->----->    *
C*      F0(I,J)|           F13(I,J,3)          |          F13(I,J,1) *
C*             |                               |                     *
C*        F11(I,J,4)                           |                     *
C*                                   F12(I,J,4)|                     *
C*                                             |                     *
C*                                             |                     *
C*                                        F13(I,J,4)                 *
C*                                                                   *
C*******************[ Sublattice Type.1 ]*****************************
C*                                                        *
C*   副格子２における粒子の種類                           *
C*                                                        *
C*                                                        *
C*                             F22(I,J,2)    F22(I,J,1)   *
C*                                 .               .      *
C*  F21(I,J,2)   F21(I,J,1)          .           .        *
C*         .       .                   .       .          *
C*           .   .                       .   .            *
C*             .                           .              *
C*           .   .                       .   .            *
C*         .       .                   .       .          *
C*  F21(I,J,2)   F21(I,J,1)          .           .        *
C*                                 .               .      *
C*                             F22(I,J,3)    F22(I,J,4)   *
C*                                                        *
C************[ Sublattice Type.2 (Diagonal Cross) ]********
C
       INCLUDE 'lbshock.inc'
C
       CALL INPUT
C
       CALL INIT
C
       DO 50 II=1,TEN
C
         WRITE(*,*) 'TIME STEP = ',II
C
         CALL COLLI
C
C         CALL COLLI2
C
         CALL MOVE1(F11,DM11,M,N)
         CALL P_BC1(F11,DM11,M,N)
C         CALL S_BC1(F11,M,N)
C
         DO 10 K=1,2
           CALL MOVE1(F12,DM12,M,N)
           CALL P_BC1(F12,DM12,M,N)
C           CALL S_BC1(F12,M,N)
   10    CONTINUE
C
         DO 20 K=1,3
           CALL MOVE1(F13,DM13,M,N)
           CALL P_BC1(F13,DM13,M,N)
C           CALL S_BC1(F13,M,N)
   20    CONTINUE
C
         CALL MOVE2(F21,DM21,M,N)
         CALL P_BC2(F21,DM21,M,N)
C         CALL S_BC2(F21,M,N)
C
         DO 30 K=1,2
           CALL MOVE2(F22,DM22,M,N)
           CALL P_BC2(F22,DM22,M,N)
C           CALL S_BC2(F22,M,N)
   30    CONTINUE
C
         IF((II.GE.TIN).AND.(MOD(II,TIN).EQ.0)) THEN
C
C-----------------------------
           CALL AVERAGE
C-----------------------------
C
         ENDIF
C
   50  CONTINUE
C
       STOP
       END
