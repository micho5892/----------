C*********************************************************************
C*       衝突過程（単一緩和時間係数 "Fai" を用いた計算）             *
C*********************************************************************
       SUBROUTINE COLLI
C---------------------------------------------------------------------
       INCLUDE 'lbshock.inc'
C
       DO 110 I=1,M-2
C    
         DO 100 J=0,N-1
C
         CALL CALE(I,J,ROU,UO,VO,E)
         CALL CALCF(C,CF,E)
C---------------------------------------------------------------------
C  
         EF0=EQUIL(UO,VO,UO*UO+VO*VO,PI,0.0E0,CF(1,0),ROU,E,0,1)
         F0(I,J)=F0(I,J)-(F0(I,J)-EF0)/FAI
         DO 95 K=1,4
           EF11=EQUIL(UO,VO,UO*UO+VO*VO,PI,      C
     &                                    ,CF(1,1),ROU,E,K,1)
           EF12=EQUIL(UO,VO,UO*UO+VO*VO,PI,2.0E0*C
     &                                    ,CF(1,2),ROU,E,K,1)
           EF13=EQUIL(UO,VO,UO*UO+VO*VO,PI,3.0E0*C
     &                                    ,CF(1,3),ROU,E,K,1)
           EF21=EQUIL(UO,VO,UO*UO+VO*VO,PI,SQRT(2.0E0)*C
     &                                    ,CF(2,1),ROU,E,K,2)
           EF22=EQUIL(UO,VO,UO*UO+VO*VO,PI,SQRT(8.0E0)*C
     &                                    ,CF(2,2),ROU,E,K,2)
           F11(I,J,K)=F11(I,J,K)-(F11(I,J,K)-EF11)/FAI
           F12(I,J,K)=F12(I,J,K)-(F12(I,J,K)-EF12)/FAI
           F13(I,J,K)=F13(I,J,K)-(F13(I,J,K)-EF13)/FAI
           F21(I,J,K)=F21(I,J,K)-(F21(I,J,K)-EF21)/FAI
           F22(I,J,K)=F22(I,J,K)-(F22(I,J,K)-EF22)/FAI
   95    CONTINUE
C---------------------------------------------------------------------
C
  100    CONTINUE
  110  CONTINUE
C
       TOTAL=0.0E0
       DO 118 I=0,M-1
         DO 117 J=0,N-1
           TOTAL=TOTAL+F0(I,J)
           DO 116 K=1,4
             TOTAL=TOTAL+F11(I,J,K)+F12(I,J,K)+F13(I,J,K)
     &                             +F21(I,J,K)+F22(I,J,K)
  116      CONTINUE
  117    CONTINUE
  118  CONTINUE
       WRITE(*,*) 'DENSITY-F-TOTAL =',TOTAL
C
       RETURN
       END
