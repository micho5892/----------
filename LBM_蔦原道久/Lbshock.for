       SUBROUTINE P_BC2(F,DMF,M,N)
C---------------------------------------------------------------------
       REAL F(0:8000,0:10,4),DMF(0:8000,0:10,4)
C
C------------------------------------------[ 上面 & 下面の境界 ]
       DO 510 I=1,M-2
         F(I,N-1,3)=DMF(I+1,0,3)
         F(I,N-1,4)=DMF(I-1,0,4)
         F(I,0,1)=DMF(I-1,N-1,1)
         F(I,0,2)=DMF(I+1,N-1,2)
  510  CONTINUE
