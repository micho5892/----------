       DO 520 J=1,N-2
         F(0,J,1)=F(0,J,2)
         F(0,J,4)=F(0,J,3)
         F(M-1,J,2)=F(M-1,J,1)
         F(M-1,J,3)=F(M-1,J,4)
  520  CONTINUE
C----------------------------------------------[ 4つのかどでの境界 ]
       F(0,N-1,4)=DMF(1,0,3)
       F(0,N-1,1)=F(0,N-1,2)
       F(M-1,N-1,3)=DMF(M-2,0,4)
       F(M-1,N-1,2)=F(M-1,N-1,1)
       F(0,0,1)=DMF(1,N-1,2)
       F(0,0,4)=F(0,0,3)
       F(M-1,0,2)=DMF(M-2,N-1,1)
       F(M-1,0,3)=F(M-1,0,4)
C---------------------------------------------------------------------
C
       RETURN
       END
