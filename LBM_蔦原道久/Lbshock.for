C*********************************************************************
C*     　流れ場での平均データの出力　　　　　                        *
C*********************************************************************
       SUBROUTINE AVERAGE
C---------------------------------------------------------------------
       INCLUDE 'lbshock.inc'
C
C
       TIME=FLOAT(II)+0.001
       KK=INT(TIME)
       K12=KK/10000000
       K11=KK-K12*10000000
       K10=K11/1000000
       KK9=K11-K10*1000000
       KK8=KK9/100000
       KK7=KK9-KK8*100000
       KK6=KK7/10000
       KK5=KK7-KK6*10000
       KK4=KK5/1000
       KK3=KK5-KK4*1000
       KK2=KK3/100
       KK1=KK3-KK2*100
       KK0=KK1/10
       KKK=KK1-KK0*10
       NAME='ssq3a'//CHAR(KK4+48)//CHAR(KK2+48)//CHAR(KK0+48)//'.ss5'
C
       OPEN(9,FILE=NAME,STATUS='UNKNOWN')
       WRITE(9,*) ROU0,DROU
       WRITE(9,*) E0,DE
       WRITE(9,*) M,N,M1
       WRITE(9,*) TEN,TIN,II
       WRITE(9,*) TAU,C
       WRITE(9,*) U1,ROU1,T1,P1,S1
       WRITE(9,*) U2,ROU2,T2,P2,S2
       WRITE(9,*) XMS,GANMA,PR,XMYU
C
       DO 470 I=0,M-1
C
         AVU(I)=0.0E0
         AVP   =0.0E0
         AVE   =0.0E0
         AVR   =0.0E0
C
         DO 460 J=1,N-2
C
           ROU=F0(I,J)
     &        +F11(I,J,1)+F11(I,J,2)+F11(I,J,3)+F11(I,J,4)
     &        +F12(I,J,1)+F12(I,J,2)+F12(I,J,3)+F12(I,J,4)
     &        +F13(I,J,1)+F13(I,J,2)+F13(I,J,3)+F13(I,J,4)
     &        +F21(I,J,1)+F21(I,J,2)+F21(I,J,3)+F21(I,J,4)
     &        +F22(I,J,1)+F22(I,J,2)+F22(I,J,3)+F22(I,J,4)
C
           ALLE=0.5E0*(F11(I,J,1)+F11(I,J,2)+F11(I,J,3)+F11(I,J,4)
     &                 +4.0E0*( F12(I,J,1)+F12(I,J,2)
     &                         +F12(I,J,3)+F12(I,J,4) )
     &                 +9.0E0*( F13(I,J,1)+F13(I,J,2)
     &                         +F13(I,J,3)+F13(I,J,4) )
     &                 +2.0E0*( F21(I,J,1)+F21(I,J,2)
     &                         +F21(I,J,3)+F21(I,J,4) )
     &                 +8.0E0*( F22(I,J,1)+F22(I,J,2)
     &                         +F22(I,J,3)+F22(I,J,4) ) )*C*C
C
           U=( F11(I,J,1)+2.0E0*F12(I,J,1)+3.0E0*F13(I,J,1)
     &        -F11(I,J,3)-2.0E0*F12(I,J,3)-3.0E0*F13(I,J,3)
     &        +( F21(I,J,1)+2.0E0*F22(I,J,1)
     &          +F21(I,J,4)+2.0E0*F22(I,J,4)
     &          -F21(I,J,2)-2.0E0*F22(I,J,2)
     &          -F21(I,J,3)-2.0E0*F22(I,J,3) ) )*C/ROU
C
           V=( F11(I,J,2)+2.0E0*F12(I,J,2)+3.0E0*F13(I,J,2)
     &        -F11(I,J,4)-2.0E0*F12(I,J,4)-3.0E0*F13(I,J,4)
     &        +( F21(I,J,1)+2.0E0*F22(I,J,1)
     &          +F21(I,J,2)+2.0E0*F22(I,J,2)
     &          -F21(I,J,3)-2.0E0*F22(I,J,3)
     &          -F21(I,J,4)-2.0E0*F22(I,J,4) ) )*C/ROU
C
           ENE=ALLE/ROU-0.5E0*(U*U+V*V)
C
           P=ROU*ENE
C
           AVU(I)=AVU(I)+  U/REAL(N-2)
           AVP   =AVP   +  P/REAL(N-2)
           AVE   =AVE   +ENE/REAL(N-2)
           AVR   =AVR   +ROU/REAL(N-2)
C
  460    CONTINUE
c       
  470  CONTINUE
C
       DO 430 I=1,M-2
         IF((AVU(I).GT.AVU(I+1)).AND.
     &      ((AVU(I).GE.0.5E0*(U1-U2)).AND.
     &      (AVU(I+1).LE.0.5E0*(U1-U2)))) THEN
           X0=I*C*TAU+(AVU(I)-0.5E0*(U1-U2))/(AVU(I)-AVU(I+1))
     &              *C*TAU
           WRITE(9,*) X0
         ENDIF
  430  CONTINUE
C
       CLOSE(9)
C
       RETURN
       END
